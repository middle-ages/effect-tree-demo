import {test, expect} from 'vitest'
import {bigCommaFormat, bigToExponential} from './Number'
import {String} from 'effect'

test('bigToExponential', () => {
  expect(bigToExponential(1_234_000n ** 100n)).toEqual([
    '1.35367986679107451849539280070606589164132035947536562028314098727325' +
      '07092101396714204188490156383876084321482018670305947277947114115523' +
      '01641030226812163007870887536846101248467081224073515912126936724561' +
      '04011300201740557442983225515412830516464664861969944121822015680394' +
      '8576381957284173320050030562955493376' +
      String.repeat(300)('0'),
    '609',
  ])
})

test('bigCommaFormat', () => {
  expect(bigCommaFormat(1_234_567_890n)).toBe('1,234,567,890')
})
